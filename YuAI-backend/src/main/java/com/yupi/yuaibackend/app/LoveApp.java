package com.yupi.yuaibackend.app;

import com.yupi.yuaibackend.advisor.MyLoggerAdvisor;
import com.yupi.yuaibackend.advisor.ReReadingAdvisor;
import com.yupi.yuaibackend.chatmemory.DatabaseChatMemory;
import com.yupi.yuaibackend.chatmemory.FileBasedChatMemory;
import com.yupi.yuaibackend.service.ChatMessageRepository;
import lombok.extern.slf4j.Slf4j;
import org.springframework.ai.chat.client.ChatClient;
import org.springframework.ai.chat.client.advisor.MessageChatMemoryAdvisor;
import org.springframework.ai.chat.memory.ChatMemory;
import org.springframework.ai.chat.memory.InMemoryChatMemory;
import org.springframework.ai.chat.model.ChatModel;
import org.springframework.ai.chat.model.ChatResponse;
import org.springframework.stereotype.Component;

import java.util.List;

import static org.springframework.ai.chat.client.advisor.AbstractChatMemoryAdvisor.CHAT_MEMORY_CONVERSATION_ID_KEY;
import static org.springframework.ai.chat.client.advisor.AbstractChatMemoryAdvisor.CHAT_MEMORY_RETRIEVE_SIZE_KEY;

/**
 * 恋爱大师APP开发
 */
@Component
@Slf4j
public class LoveApp {

    private final ChatClient chatClient;

    // 系统预设 - 提示词
    private static final String SYSTEM_PROMPT = "扮演深耕恋爱心理领域的专家。 开场向用户表明身份，告知用户可倾诉恋爱难题。"
            + "围绕单身、恋爱、已婚三种状态提问：单身状态询问社交圈拓展及追求心仪对象的困扰；"
            + "恋爱状态询问沟通、习惯差异引发的矛盾；已婚状态询问家庭责任与亲属关系处理的问题"
            + "引导用户详述事情经过、对方反应及自身想法，以便给出专属解决方案。";


    /**
     * 构造函数，注入依赖
     *
     * @param dashscopeChatModel
     * @param chatMessageRepository
     */
    public LoveApp(ChatModel dashscopeChatModel, ChatMessageRepository chatMessageRepository) {

        // 初始化基于MySQL的对话记忆
        DatabaseChatMemory chatMemory = new DatabaseChatMemory(chatMessageRepository);

        // 初始化基于文件的对话记忆
        // String fileDir = System.getProperty("user.dir") + "/tmp/chat-memory";
        // FileBasedChatMemory chatMemory = new FileBasedChatMemory(fileDir);

        // 初始化基于内存的对话记忆
        // ChatMemory chatMemory = new InMemoryChatMemory();
        chatClient = ChatClient.builder(dashscopeChatModel)
                // 指定系统预设
                .defaultSystem(SYSTEM_PROMPT)
                // 指定默认拦截器
                .defaultAdvisors(
                        new MessageChatMemoryAdvisor(chatMemory),
                        // 自定义日志 Advisor，可按需开启
                        new MyLoggerAdvisor()
                        // 自定义推理增强 Advisor，可按需开启
                        // new ReReadingAdvisor()
                )
                .build();
    }





    /**
     * 编写聊天方法
     * @param message
     * @param chatId
     * @return
     */
    public String doChat(String message, String chatId) {
        ChatResponse chatResponse = chatClient
                .prompt()
                .user(message)
                .advisors(spec -> spec.param(CHAT_MEMORY_CONVERSATION_ID_KEY, chatId)
                        .param(CHAT_MEMORY_RETRIEVE_SIZE_KEY, 10))
                .call()
                .chatResponse();

        String content = chatResponse.getResult().getOutput().getText();
        log.info("content: {}", content);
        return content;
    }

    // java21新特性：使用record记录特性 快速定义变量，相当于定义一个类
    record LoveReport(String title, List<String> suggestions) {

    }

    /**
     * AI恋爱报告功能（实战结构化输出）
     * @param message
     * @param chatId
     * @return
     */
    public LoveReport doChatWithReport(String message, String chatId) {
        LoveReport loveReport = chatClient
                .prompt()
                // 系统预设
                .system("SYSTEM_PROMPT" + "每次对话后都要生成恋爱结果，标题为{用户名}的恋爱报告，内容为建议列表")
                .user(message)
                .advisors(spec -> spec.param(CHAT_MEMORY_CONVERSATION_ID_KEY, chatId)
                        .param(CHAT_MEMORY_RETRIEVE_SIZE_KEY, 10))
                .call()
                .entity(LoveReport.class);

        log.info("loveRepost: {}", loveReport);
        return loveReport;
    }
}
